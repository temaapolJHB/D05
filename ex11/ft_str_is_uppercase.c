int ft_str_is_uppercase(char *str)
{
	int j;
   
	j = 0;
	while(str[j] != '\0')
	{
    	if (!(str[j] >= 'A' && str[0] <= 'Z'))
	    {
	        return (0);
     	}
	 j++;
	}
      return (1);
}

